package com.yash.teacoffee.vendingmachine.Model;

public class WasteMaterial {
	private int tea;
	private int coffee;
	private int sugar;
	private int water;
	private int milk;

	public int getTea() {
		return tea;
	}

	public void setTea(int tea) {
		this.tea = tea;
	}

	public int getCoffee() {
		return coffee;
	}

	public void setCoffee(int coffee) {
		this.coffee = coffee;
	}

	public int getSugar() {
		return sugar;
	}

	public void setSugar(int sugar) {
		this.sugar = sugar;
	}

	public int getWater() {
		return water;
	}

	public void setWater(int water) {
		this.water = water;
	}

	public int getMilk() {
		return milk;
	}

	public void setMilk(int milk) {
		this.milk = milk;
	}

}
